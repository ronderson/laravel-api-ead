<?php

namespace App\Models\Traits;
use Illuminate\Support\Str;

trait UuidTrait
{
    protected static function booted()
    {
        static::creating(function ($model) {
            if (empty($model->{$model->getKeyName()})) {
                $model->{$model->getKeyName()} = (string) Str::uuid();
            }
        });
    }
}
